$query = "
CREATE TABLE  IF NOT EXISTS `#__jomres_reviews_ratings_replies` (
	`rating_id` INT UNSIGNED NOT NULL AUTO_INCREMENT ,
	`replier_id` INT UNSIGNED NOT NULL DEFAULT 0,
	`replier_name` VARCHAR(255),
	`reply` TEXT,
	PRIMARY KEY (`rating_id`)
	)
	ENGINE = InnoDB 
	DEFAULT CHARSET = utf8mb4 
	COLLATE = utf8mb4_unicode_ci;
";

if (!doInsertSql($query)) {
	$this->setMessage('Error, unable to create the #__jomres_reviews_ratings_replies table', 'danger');
}
