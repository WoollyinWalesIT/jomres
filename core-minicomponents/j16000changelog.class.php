			$this->template_touchable = false;

			return;
		}
		$changelog = nl2br(file_get_contents('http://updates.jomres.net/CHANGELOG_JOMRES'));
		echo $changelog;
	}


	public function getRetVals()
	{
		return null;
	}
}
